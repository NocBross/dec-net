package main.java.constants;

public class SecretKeys {

    /**
     * Defines which character is used to define a comment line.
     */
    public static final byte COMMENT_KEY = '#';

    /**
     * Defines the key which binds the database user value.
     */
    public static final String DATABASE_USER = "user";

    /**
     * Defines the key which binds the database password value.
     */
    public static final String DATABASE_PASSWORD = "password";

    /**
     * Defines the character which splits the key and the value.
     */
    public static final String KEY_VALUE_SEPARATOR = "=";

}
